class Weapon:
    def __init__(self, name, damage, fire_rate, accuracy, range, magazine_size, reload_time):
        self.name          = name
        self.damage        = damage
        self.fire_rate     = fire_rate
        self.accuracy      = accuracy
        self.range         = range
        self.magazine_size = magazine_size
        self.reload_time   = reload_time
